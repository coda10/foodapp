//Import Router package
const router = require('express').Router();

//Import Admin Dashboard Controller
const adminDashboard = require('../../controller/adminDashboardController');

//Import token Function module
const auth = require('../../helperFunctions/tokenFunction');

//===================================================================================================================//
//                                      {Add Student Endpoint}
//===================================================================================================================//
router.post('/addstudent', auth.verifyToken, adminDashboard.addstudent);

//===================================================================================================================//
//                                      {View All Students Endpoint}
//===================================================================================================================//
router.get('/viewallstudents', auth.verifyToken, adminDashboard.viewAllStudents);
//===================================================================================================================//
//                                      {Edit Student Endpoint}
//===================================================================================================================//
router.patch('/editstudent', auth.verifyToken, adminDashboard.editStudent);

//===================================================================================================================//
//                                      {Delete Student Endpoint}
//===================================================================================================================//
router.delete('/deletestudent', auth.verifyToken, adminDashboard.deleteStudent);

//===================================================================================================================//
//                                      {Create Menu Endpoint}
//===================================================================================================================//
router.post('/createmenu', auth.verifyToken, adminDashboard.createMenu);

//===================================================================================================================//
//                                      {Load All Menu Endpoint}
//===================================================================================================================//
router.get('/Viewallmenu', auth.verifyToken, adminDashboard.ViewallMenu);

//===================================================================================================================//
//                                      {Edit Menu Endpoint}
//===================================================================================================================//
router.patch('/editmenu', auth.verifyToken, adminDashboard.editMenu);

//===================================================================================================================//
//                                      {Delete Menu Endpoint}
//===================================================================================================================//
router.delete('/deletemenu', auth.verifyToken, adminDashboard.deleteMenu);

//===================================================================================================================//
//                                      {view Orders Endpoint}
//===================================================================================================================//
router.get('/vieworders', auth.verifyToken, adminDashboard.viewOrders);


//Export Router Module
module.exports = router;